create table if not exists public.manager_result_submissions (
  id bigint generated by default as identity primary key,
  match_id bigint not null unique references public.matches(id) on delete cascade,
  submitted_by_user_id uuid not null references auth.users(id) on delete cascade,
  submitted_by_manager_id bigint not null references public.managers(id) on delete cascade,
  submitted_home_score integer not null check (submitted_home_score >= 0 and submitted_home_score <= 99),
  submitted_away_score integer not null check (submitted_away_score >= 0 and submitted_away_score <= 99),
  status text not null default 'pending_confirmation' check (status in ('pending_confirmation','confirmed','disputed','resolved','withdrawn')),
  opponent_user_id uuid references auth.users(id) on delete set null,
  opponent_manager_id bigint references public.managers(id) on delete set null,
  opponent_response_note text,
  confirmed_at timestamptz,
  disputed_at timestamptz,
  resolved_by uuid references auth.users(id) on delete set null,
  resolved_home_score integer check (resolved_home_score >= 0 and resolved_home_score <= 99),
  resolved_away_score integer check (resolved_away_score >= 0 and resolved_away_score <= 99),
  resolution_note text,
  resolved_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.manager_result_submissions enable row level security;

create policy "Managers can read submissions for their matches"
  on public.manager_result_submissions for select to authenticated
  using (
    public.is_admin()
    or submitted_by_user_id = auth.uid()
    or opponent_user_id = auth.uid()
  );

create policy "Admins can manage result submissions"
  on public.manager_result_submissions for all to authenticated
  using (public.is_admin())
  with check (public.is_admin());

create or replace function public.submit_manager_result(
  target_match_id bigint,
  target_home_score integer,
  target_away_score integer
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  account_row public.manager_portal_accounts%rowtype;
  match_row public.matches%rowtype;
  submitter_entry public.tournament_entries%rowtype;
  opponent_entry public.tournament_entries%rowtype;
  opponent_account public.manager_portal_accounts%rowtype;
  submission_id bigint;
begin
  if target_home_score < 0 or target_away_score < 0 then raise exception 'Scores cannot be negative'; end if;

  select * into account_row from public.manager_portal_accounts
  where auth_user_id = auth.uid() and active = true;
  if not found then raise exception 'Manager Portal account not found'; end if;

  select * into match_row from public.matches where id = target_match_id for update;
  if not found then raise exception 'Match not found'; end if;
  if match_row.status in ('played','forfeit') then raise exception 'This match already has an official result'; end if;

  select * into submitter_entry from public.tournament_entries
  where manager_id = account_row.manager_id
    and id in (match_row.home_entry_id, match_row.away_entry_id)
  limit 1;
  if not found then raise exception 'You are not a manager in this fixture'; end if;

  select * into opponent_entry from public.tournament_entries
  where id = case when submitter_entry.id = match_row.home_entry_id then match_row.away_entry_id else match_row.home_entry_id end;

  select * into opponent_account from public.manager_portal_accounts
  where manager_id = opponent_entry.manager_id and active = true
  limit 1;

  insert into public.manager_result_submissions (
    match_id, submitted_by_user_id, submitted_by_manager_id,
    submitted_home_score, submitted_away_score,
    opponent_user_id, opponent_manager_id, status, updated_at
  ) values (
    target_match_id, auth.uid(), account_row.manager_id,
    target_home_score, target_away_score,
    opponent_account.auth_user_id, opponent_entry.manager_id,
    'pending_confirmation', now()
  )
  on conflict (match_id) do update set
    submitted_by_user_id = excluded.submitted_by_user_id,
    submitted_by_manager_id = excluded.submitted_by_manager_id,
    submitted_home_score = excluded.submitted_home_score,
    submitted_away_score = excluded.submitted_away_score,
    opponent_user_id = excluded.opponent_user_id,
    opponent_manager_id = excluded.opponent_manager_id,
    opponent_response_note = null,
    status = 'pending_confirmation',
    confirmed_at = null,
    disputed_at = null,
    resolved_by = null,
    resolved_home_score = null,
    resolved_away_score = null,
    resolution_note = null,
    resolved_at = null,
    updated_at = now()
  returning id into submission_id;

  return submission_id;
end;
$$;

create or replace function public.respond_to_manager_result(
  target_submission_id bigint,
  response text,
  note text default null
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  submission_row public.manager_result_submissions%rowtype;
  match_row public.matches%rowtype;
  winner_id bigint;
  loser_id bigint;
begin
  select * into submission_row from public.manager_result_submissions
  where id = target_submission_id for update;
  if not found then raise exception 'Result submission not found'; end if;
  if submission_row.opponent_user_id is distinct from auth.uid() then raise exception 'Only the opposing manager can respond'; end if;
  if submission_row.status <> 'pending_confirmation' then raise exception 'This submission is no longer awaiting confirmation'; end if;

  if response = 'confirm' then
    select * into match_row from public.matches where id = submission_row.match_id for update;
    winner_id := case when submission_row.submitted_home_score > submission_row.submitted_away_score then match_row.home_entry_id when submission_row.submitted_away_score > submission_row.submitted_home_score then match_row.away_entry_id else null end;
    loser_id := case when submission_row.submitted_home_score > submission_row.submitted_away_score then match_row.away_entry_id when submission_row.submitted_away_score > submission_row.submitted_home_score then match_row.home_entry_id else null end;

    update public.matches set
      home_score = submission_row.submitted_home_score,
      away_score = submission_row.submitted_away_score,
      winner_entry_id = winner_id,
      loser_entry_id = loser_id,
      status = 'played',
      played_at = now()
    where id = submission_row.match_id;

    update public.manager_result_submissions set
      status = 'confirmed', opponent_response_note = note,
      confirmed_at = now(), updated_at = now()
    where id = target_submission_id;
  elsif response = 'dispute' then
    update public.manager_result_submissions set
      status = 'disputed', opponent_response_note = note,
      disputed_at = now(), updated_at = now()
    where id = target_submission_id;
  else
    raise exception 'Response must be confirm or dispute';
  end if;
end;
$$;

create or replace function public.resolve_manager_result(
  target_submission_id bigint,
  target_home_score integer,
  target_away_score integer,
  note text default null
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  submission_row public.manager_result_submissions%rowtype;
  match_row public.matches%rowtype;
  winner_id bigint;
  loser_id bigint;
begin
  if not public.is_admin() then raise exception 'Admin access required'; end if;
  if target_home_score < 0 or target_away_score < 0 then raise exception 'Scores cannot be negative'; end if;

  select * into submission_row from public.manager_result_submissions where id = target_submission_id for update;
  if not found then raise exception 'Result submission not found'; end if;
  select * into match_row from public.matches where id = submission_row.match_id for update;

  winner_id := case when target_home_score > target_away_score then match_row.home_entry_id when target_away_score > target_home_score then match_row.away_entry_id else null end;
  loser_id := case when target_home_score > target_away_score then match_row.away_entry_id when target_away_score > target_home_score then match_row.home_entry_id else null end;

  update public.matches set
    home_score = target_home_score,
    away_score = target_away_score,
    winner_entry_id = winner_id,
    loser_entry_id = loser_id,
    status = 'played',
    played_at = now()
  where id = submission_row.match_id;

  update public.manager_result_submissions set
    status = 'resolved', resolved_by = auth.uid(),
    resolved_home_score = target_home_score,
    resolved_away_score = target_away_score,
    resolution_note = note, resolved_at = now(), updated_at = now()
  where id = target_submission_id;
end;
$$;

grant execute on function public.submit_manager_result(bigint, integer, integer) to authenticated;
grant execute on function public.respond_to_manager_result(bigint, text, text) to authenticated;
grant execute on function public.resolve_manager_result(bigint, integer, integer, text) to authenticated;
